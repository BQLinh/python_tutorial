# ví dụ 1

def greet(): # function này để hiện thị 1 lời chào 
    message = 'Hello this is Linh' # khai báo biến message với nội dung/giá trị là 'Hello this is Linh'
    print(message) # print là câu lệnh để in ra màn hình

